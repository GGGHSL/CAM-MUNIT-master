class StyleEncoder(nn.Module):
    def __init__(self, n_downsample, input_dim, dim, style_dim, norm, activ, pad_type):
        super(StyleEncoder, self).__init__()
        self.model = []
        self.model += [Conv2dBlock(input_dim, dim, 7, 1, 3, norm=norm, activation=activ, pad_type=pad_type)]
        for i in range(2):
            self.model += [Conv2dBlock(dim, 2 * dim, 4, 2, 1, norm=norm, activation=activ, pad_type=pad_type)]
            dim *= 2
        for i in range(n_downsample - 2):
            self.model += [Conv2dBlock(dim, dim, 4, 2, 1, norm=norm, activation=activ, pad_type=pad_type)]

        self.model += [nn.AdaptiveAvgPool2d(1)]  # global average pooling
        self.model += [nn.Conv2d(dim, style_dim, 1, 1, 0)]
        self.model = nn.Sequential(*self.model)
        self.output_dim = dim

    def forward(self, x):
        return self.model(x)


class ContentEncoder(nn.Module):
    def __init__(self, n_downsample, n_res, input_dim, dim, norm, activ, pad_type):
        super(ContentEncoder, self).__init__()
        self.model = []
        self.model += [Conv2dBlock(input_dim, dim, 7, 1, 3, norm=norm, activation=activ, pad_type=pad_type)]

        # down-sampling blocks
        for i in range(n_downsample):
            self.model += [Conv2dBlock(dim, 2 * dim, 4, 2, 1, norm=norm, activation=activ, pad_type=pad_type)]
            dim *= 2

        # residual blocks
        self.model += [ResBlocks(n_res, dim, norm=norm, activation=activ, pad_type=pad_type)]
        self.model = nn.Sequential(*self.model)
        self.output_dim = dim  # 256

        # --- for cam
        self.gap_fc = nn.Linear(dim, 1, bias=False)
        self.gmp_fc = nn.Linear(dim, 1, bias=False)
        self.conv1x1 = nn.Conv2d(dim * 2, dim, kernel_size=1, stride=1, bias=True)  # 512 -> 256
        self.relu = nn.ReLU(True)
        self.leaky_relu = nn.LeakyReLU(0.2)

        # --- for adv classify
        self.pad = nn.ReplicationPad2d(1)
        self.conv_sn = nn.utils.spectral_norm(nn.Conv2d(dim, 1, kernel_size=4, stride=1, padding=0, bias=False))

    def forward(self, x):
        content = self.model(x)

        gap_c_x = F.adaptive_avg_pool2d(content, 1)
        gmp_c_x = F.adaptive_avg_pool2d(content, 1)
        gap_c_x_logit = self.gap_fc(gap_c_x.view(gap_c_x.size(0), -1))
        gmp_c_x_logit = self.gap_fc(gmp_c_x.view(gmp_c_x.size(0), -1))
        cam_c_x_logit = torch.cat([gap_c_x_logit, gmp_c_x_logit], 1)

        gap_c_x_w = list(self.gap_fc.parameters())[0]
        gmp_c_x_w = list(self.gmp_fc.parameters())[0]
        cam_gap_c_x = content * gap_c_x_w.unsqueeze(2).unsqueeze(3)
        cam_gmp_c_x = content * gmp_c_x_w.unsqueeze(2).unsqueeze(3)
        cam_c_x = torch.cat([cam_gap_c_x, cam_gmp_c_x], 1)
        cam_c_x = self.relu(self.conv1x1(cam_c_x))  # without activation: do when update

        return cam_c_x, cam_c_x_logit


class Decoder(nn.Module):
    def __init__(self, n_upsample, n_res, dim, output_dim, res_norm='adain', activ='relu', pad_type='zero'):
        super(Decoder, self).__init__()

        self.model = []
        # AdaIN residual blocks
        self.model += [ResBlocks(n_res, dim, res_norm, activ, pad_type=pad_type)]

        # upsampling blocks
        for i in range(n_upsample):
            self.model += [nn.Upsample(scale_factor=2),
                           Conv2dBlock(dim, dim // 2, 5, 1, 2,  # c减半, (h, w)不变
                                       norm='ln', activation=activ, pad_type=pad_type)]
            dim //= 2

        # use reflection padding in the last conv layer: c=64 -> c=3
        self.model += [Conv2dBlock(dim, output_dim, 7, 1, 3, norm='none', activation='tanh', pad_type=pad_type)]
        self.model = nn.Sequential(*self.model)

    def forward(self, x):
        out = self.model(x)  # [1, 3, 256, 256]
        return out


